        public void readTable() { 
        DefaultTableModel modelo = (DefaultTableModel) tableUser.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();
        
        for (Usuario u : udao.read()) {
            modelo.addRow(new Object[]{
                u.getNome(),
                u.getCargo(),
                u.getEmail(),
                u.getLogin(),
                u.getSenha()
            });
        }
    }

